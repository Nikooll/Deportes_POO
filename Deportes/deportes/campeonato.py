
class Campeonato:
    def __init__(self, duracion_compromiso, fecha_inicio, fecha_fin, genero, numero_clasificados_por_grupo, numero_grupos_por_grupo, numero_equipos, numero_vueltas):
        self.duracion_compromiso = duracion_compromiso
        self.fecha_inicio = fecha_inicio
        self.fecha_fin = fecha_fin
        self.genero = genero
        self.numero_clasificados_por_grupo = numero_clasificados_por_grupo
        self.numero_grupos_por_grupo = numero_grupos_por_grupo
        self.numero_equipos = numero_equipos
        self.numero_vueltas = numero_vueltas
